const input = prompt("Masukkan Nilai Anda :");

if (input <= 100 && input >= 90) {
    alert("Nilai anda A");
} else if (input >= 80) {
    alert("Nilai anda B");
} else if (input >= 70) {
    alert("Nilai anda C");
} else if (input >= 60) {
    alert("Nilai anda D");
} else {
    alert("Nilai anda E");
}
